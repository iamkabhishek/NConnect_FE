// Helper function to get published templates for a workspace
export const getPublishedTemplates = (workspaceId: string) => {
  return ALL_TEMPLATES.filter(
    (template) => template.workspaceId === workspaceId && template.status === 'published'
  );
};

// Helper function to get all templates for a workspace
export const getWorkspaceTemplates = (workspaceId: string) => {
  return ALL_TEMPLATES.filter((template) => template.workspaceId === workspaceId);
};
